export ENABLE_USB_CAMERA_RTSP USB_CAMERA_RTSP_ENABLE USB_CAMERA_RTSP_CAMERAS USB_CAMERA_WIDTH USB_CAMERA_HEIGHT USB_CAMERA_FPS USB_CAMERA_INPUT_FORMAT USB_CAMERA_RTSP_BITRATE_KBPS
