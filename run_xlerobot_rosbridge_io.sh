echo "camera    : ${ENABLE_CAMERA} (compressed only)"
if [[ "$ENABLE_CAMERA" == "true" || "$ENABLE_CAMERA" == "1" ]]; then
  echo "camera dev: ${CAMERA_DEVICE}"
  echo "camera pub: ${CAMERA_TOPIC}"
