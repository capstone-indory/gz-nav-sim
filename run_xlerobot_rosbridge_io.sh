  DEPTH_SENSOR_JPEG_QUALITY DEPTH_SENSOR_PNG_COMPRESS_LEVEL
  DEPTH_SENSOR_RTSP_ENABLE DEPTH_SENSOR_RTSP_URL DEPTH_SENSOR_RTSP_FPS
